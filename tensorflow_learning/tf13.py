import numpy as np

#mgrid 里面有 几个 起始结束步长 就返回几个 等差数组
x,y = np.mgrid[1:3:1,2:4:0.5]

grid = np.c_[x.ravel(),y.ravel()]
print(x)
print(x.ravel())
print(y)
print(y.ravel())
print(grid)

